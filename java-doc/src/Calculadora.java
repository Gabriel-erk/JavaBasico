public class Calculadora {
    /** 
     * este método retorna a soma de dois numeros
     * @param numeroUm - este é o primeiro parâmetro
     * @param numeroDois - este é o segundo parâmetro
     * documentando de forma especializada, ao passar o mouse sobre o nome do método, mostrará o nome, dunção do método a classe pai, e a descrição dos parâmetros
    */
    public int soma(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
}